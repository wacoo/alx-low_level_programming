/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * 
 * Return: sum of a and b
 */

int add (int a, int b)
{
	return a + b;
}
/**
 * sub - subtracts b from a
 * @a: first number
 * @b: second number
 *
 * Return: a - b
 */

int sub (int a, int b)
{
        return a - b;
}
/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a * b
 */

int mul (int a, int b)
{
        return a * b;
}
/**
 * div - devides a to b
 * @a: first number
 * @b: second number
 *
 * Return: a / b
 */

int div (int a, int b)
{
        return a / b;
}
/**
 * mod - returns a modulo b
 * @a: first number
 * @b: second number
 *
 * Return: modulo of a and b
 */

int mod (int a, int b)
{
        return a % b;
}

